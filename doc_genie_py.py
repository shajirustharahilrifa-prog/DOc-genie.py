class DocGenieAnalyzer:
    """Analyze Python functions and generate professional docstrings"""

    @staticmethod
    def extract_function_signature(code):
        """Extract function name, parameters, and return type from code."""
        try:
            tree = ast.parse(code)
            func_defs = [node for node in tree.body if isinstance(node, ast.FunctionDef)]

            if not func_defs:
                return None, None, None # No function definition found

            func_def = func_defs[0] # Take the first function definition if multiple exist
            func_name = func_def.name
            params = []

            # Handle parameters
            for i, arg in enumerate(func_def.args.args):
                param_info = {"name": arg.arg, "type": "Any", "default": None}
                if arg.annotation:
                    param_info["type"] = ast.unparse(arg.annotation) if hasattr(ast, 'unparse') else "Any"

                # Handle default values (only for args with defaults)
                num_defaults = len(func_def.args.defaults)
                if num_defaults > 0 and i >= len(func_def.args.args) - num_defaults:
                    default_value_node = func_def.args.defaults[i - (len(func_def.args.args) - num_defaults)]
                    param_info["default"] = ast.unparse(default_value_node) if hasattr(ast, 'unparse') else None
                params.append(param_info)

            # Handle return type
            return_type = "Any"
            if func_def.returns:
                return_type = ast.unparse(func_def.returns) if hasattr(ast, 'unparse') else "Any"

            return func_name, params, return_type

        except SyntaxError as e:
            print(f"SyntaxError in code: {e}")
            return None, None, None # Handle invalid code input
        except Exception as e:
            print(f"Error during signature extraction: {e}")
            return None, None, None

    @staticmethod
    def analyze_function_logic(func_def_node):
        """Deep analysis of a function's AST node to understand its behavior."""
        analysis = {
            'has_loops': False,
            'has_conditions': False,
            'has_return': False,
            'loop_types': set(), # Using set to avoid duplicates
            'condition_types': set(), # Using set to avoid duplicates
            'operations': set(),
            'return_values': set(),
            'variables_used': set(),
            'function_calls': set(),
            'description': ""
        }

        for node in ast.walk(func_def_node):
            if isinstance(node, (ast.For, ast.While)):
                analysis['has_loops'] = True
                analysis['loop_types'].add(type(node).__name__.lower())
            elif isinstance(node, (ast.If, ast.IfExp)):
                analysis['has_conditions'] = True
                analysis['condition_types'].add(type(node).__name__.lower())
            elif isinstance(node, ast.Return):
                analysis['has_return'] = True
                if node.value:
                    analysis['return_values'].add(ast.unparse(node.value) if hasattr(ast, 'unparse') else "unknown value")
            elif isinstance(node, ast.Name) and isinstance(node.ctx, (ast.Load, ast.Store)):
                analysis['variables_used'].add(node.id)
            elif isinstance(node, ast.Call):
                if isinstance(node.func, ast.Name):
                    analysis['function_calls'].add(node.func.id)
                elif isinstance(node.func, ast.Attribute):
                    # For method calls like obj.method(), extract 'method'
                    analysis['function_calls'].add(node.func.attr)

        # Generate a descriptive summary
        description_parts = []
        if analysis['has_loops']:
            description_parts.append(f"It contains loops ({', '.join(sorted(analysis['loop_types']))}).")
        if analysis['has_conditions']:
            description_parts.append(f"It uses conditional statements ({', '.join(sorted(analysis['condition_types']))}).")
        if analysis['has_return']:
            returned_values = ', '.join(sorted(analysis['return_values']))
            description_parts.append(f"It returns a value{f', potentially: {returned_values}' if returned_values else ''}.")
        if analysis['variables_used']:
            description_parts.append(f"It uses variables such as: {', '.join(sorted(analysis['variables_used']))}.")
        if analysis['function_calls']:
            description_parts.append(f"It makes calls to functions like: {', '.join(sorted(analysis['function_calls']))}.")

        if not description_parts:
            description_parts.append("A simple function with no apparent loops, conditions, or function calls.")

        analysis['description'] = " ".join(description_parts)

        return analysis

