def generate_full_output(code, style):
    try:
        analyzer = DocGenieAnalyzer()
        name, params, ret = analyzer.extract_function_signature(code)
        if not name:
            return "# Error: No valid function definition found."

        tree = ast.parse(code)
        func_node = [n for n in tree.body if isinstance(n, ast.FunctionDef)][0]
        logic = analyzer.analyze_function_logic(func_node)

        if style == "google":
            docstring = generate_google_docstring({'params': params, 'return_type': ret}, logic)
        else:
            docstring = generate_numpy_docstring({'params': params, 'return_type': ret}, logic)

        # Insert docstring after function signature
        lines = code.split('\n')
        lines.insert(1, docstring)
        return '\n'.join(lines)
    except Exception as e:
        return f"# Error during generation: {str(e)}"

